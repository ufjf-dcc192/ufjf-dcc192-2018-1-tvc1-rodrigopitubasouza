package Classes;

/**
 *
 * @author rodri
 */
public class Lingua {
    String nome;
    int id;
    public Lingua() {
    }

    public Lingua(int id,String nome) {
        this.nome = nome;
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return nome;
    }
    
    
}
